    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Company  $company
     * @return \Illuminate\Http\Response
     */
    public function destroy(Company $company)
    {
        if($company->trashed()){
            $company->restore();
        }
        else{
            $company->delete();
        }
        
        return response([
            'data' => new CompanyResource($company->fresh())
        ], Response::HTTP_ACCEPTED);
    }
